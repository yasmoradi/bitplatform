namespace Bit.BlazorUI;

public class BitToggleButtonClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitToggleButton.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the check mark element of the BitToggleButton.
    /// </summary>
    public string? CheckMark { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the icon element of the BitToggleButton.
    /// </summary>
    public string? Icon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the checked state of the BitToggleButton.
    /// </summary>
    public string? Checked { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the container of the hidden content of the BitToggleButton in the loading state.
    /// </summary>
    public string? HiddenContent { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the loading container of the BitToggleButton.
    /// </summary>
    public string? LoadingContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the loading label of the BitToggleButton.
    /// </summary>
    public string? LoadingLabel { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the loading spinner of the BitToggleButton.
    /// </summary>
    public string? Spinner { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the text element of the BitToggleButton.
    /// </summary>
    public string? Text { get; set; }
}
