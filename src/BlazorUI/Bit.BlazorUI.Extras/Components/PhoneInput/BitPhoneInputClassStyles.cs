namespace Bit.BlazorUI;

public class BitPhoneInputClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitPhoneInput.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the label of the BitPhoneInput.
    /// </summary>
    public string? Label { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the field group (the container of the country dropdown and the number input) of the BitPhoneInput.
    /// </summary>
    public string? FieldGroup { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? Dropdown { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the overlay of the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? Overlay { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the callout of the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? Callout { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the search box of the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? SearchBox { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the scroll container of the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? ScrollContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the selected country text (flag and dialing code) of the BitPhoneInput.
    /// </summary>
    public string? DropdownText { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the name of each item in the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? ItemName { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the dialing code of each item in the country dropdown of the BitPhoneInput.
    /// </summary>
    public string? ItemCode { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the number input (text field) of the BitPhoneInput.
    /// </summary>
    public string? Input { get; set; }
}
