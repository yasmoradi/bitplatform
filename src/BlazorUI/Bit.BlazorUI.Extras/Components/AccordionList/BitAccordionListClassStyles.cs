namespace Bit.BlazorUI;

/// <summary>
/// Custom CSS classes/styles for different parts of the <see cref="BitAccordionList{TItem}"/>.
/// </summary>
public class BitAccordionListClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitAccordionList.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each accordion item of the BitAccordionList.
    /// </summary>
    public string? Item { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the expanded state of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemExpanded { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemHeader { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header content of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemHeaderContent { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the title of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemTitle { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the description of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemDescription { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the expander icon wrapper of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemExpanderIconWrapper { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the expander icon of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemExpanderIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the expander icon of each accordion item of the BitAccordionList in the expanded state.
    /// </summary>
    public string? ItemExpandedIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the content container of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemContentContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the content of each accordion item of the BitAccordionList.
    /// </summary>
    public string? ItemContent { get; set; }
}
